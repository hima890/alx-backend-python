#!/usr/bin/python3
"""
A type-annotated function concat that takes a string str1 and a string str2 as
arguments and returns a concatenated string
"""


def concat(str1: str, str2: str) -> str:
    """
    Function Name: concat

    Description:
        Function concat that takes a string str1 and a string str2 as
        arguments and returns a concatenated string

    Args:
        str1 (str): str input
        str2 (str): str input

    Returns:
        str: A concatenated string
    """
    return str1 + str2
